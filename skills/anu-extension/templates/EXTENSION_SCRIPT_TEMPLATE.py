def validate_series(df: pd.DataFrame, config: Dict) -> Dict:
    """Validate the extended series."""
    results = {
        "timestamp": datetime.now().isoformat(),
        "checks": [],
        "overall": "PASS",
    }
    
    # Check year range
    if not df.empty and "year" in df.columns:
        actual_start = df["year"].min()
        actual_end = df["year"].max()
        expected_start = config["original_period"][0]
        expected_end = config["extension_period"][1]
        
        year_check = {
            "name": "year_range",
            "expected": f"{expected_start}-{expected_end}",
            "actual": f"{actual_start}-{actual_end}",
            "status": "PASS" if actual_start <= expected_start and actual_end >= expected_end else "WARN",
        }
        results["checks"].append(year_check)
    
    # Check value bounds
    if "expected_range" in config and config["expected_range"]["min"] is not None:
        min_val = df["value"].min() if "value" in df.columns else None
        max_val = df["value"].max() if "value" in df.columns else None
        
        bounds_check = {
            "name": "value_bounds",
            "expected_min": config["expected_range"]["min"],
            "expected_max": config["expected_range"]["max"],
            "actual_min": min_val,
            "actual_max": max_val,
            "status": "PASS",
        }
        
        if min_val is not None:
            if config["expected_range"]["min"] is not None and min_val < config["expected_range"]["min"]:
                bounds_check["status"] = "WARN"
            if config["expected_range"]["max"] is not None and max_val > config["expected_range"]["max"]:
                bounds_check["status"] = "WARN"
        
        results["checks"].append(bounds_check)
    
    # Check for missing values
    if "value" in df.columns:
        missing_count = df["value"].isna().sum()
        missing_check = {
            "name": "missing_values",
            "count": int(missing_count),
            "status": "PASS" if missing_count == 0 else "WARN",
        }
        results["checks"].append(missing_check)
    
    # Determine overall status
    if any(c["status"] == "FAIL" for c in results["checks"]):
        results["overall"] = "FAIL"
    elif any(c["status"] == "WARN" for c in results["checks"]):
        results["overall"] = "WARN"
    
    return results


