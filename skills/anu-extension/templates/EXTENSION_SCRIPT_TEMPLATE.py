def fetch_api_data(config: Dict, fetcher: UnifiedDataFetcher) -> Tuple[pd.DataFrame, Dict]:
    """Fetch data from API and return with provenance metadata."""
    api_spec = config["api"]
    start_year, end_year = config["period"]
    
    # Fetch data
    fetch_start = datetime.now()
    df = fetcher.get_series(api_spec, start_year, end_year)
    fetch_end = datetime.now()
    
    # Generate provenance
    provenance = {
        "api_endpoint": api_spec,
        "fetch_timestamp": fetch_start.isoformat(),
        "fetch_duration_ms": (fetch_end - fetch_start).total_seconds() * 1000,
        "rows_fetched": len(df) if df is not None else 0,
        "response_hash": hashlib.sha256(str(df).encode()).hexdigest()[:16] if df is not None else None,
    }
    
    return df, provenance


def load_subsource(subsource_id: str, config: Dict, fetcher: Optional[UnifiedDataFetcher] = None) -> Tuple[pd.DataFrame, Dict]:
    """Load a subsource based on its type."""
    source_type = config.get("type", "unknown")
    provenance = {"subsource_id": subsource_id, "type": source_type}
    
    if source_type == "historical":
        df = load_chopped_table(config)
        provenance["file"] = config["file"]
        
    elif source_type == "shaikh_original":
        df = load_shaikh_baseline(config)
        provenance["file"] = config["file"]
        
    elif source_type in ["fred", "bea_nipa", "bea_fixed_assets", "bea_gdp_industry"]:
        if fetcher is None:
            raise RuntimeError("Fetcher required for API subsources")
        df, api_prov = fetch_api_data(config, fetcher)
        provenance.update(api_prov)
        
    elif source_type == "calculated":
        # Calculated subsources are derived later
        df = pd.DataFrame()
        provenance["note"] = "Calculated from other subsources"
        
    else:
        raise ValueError(f"Unknown subsource type: {source_type}")
    
    # Filter to period
    if "period" in config and not df.empty and "year" in df.columns:
        start, end = config["period"]
        df = df[(df["year"] >= start) & (df["year"] <= end)]
    
    return df, provenance


