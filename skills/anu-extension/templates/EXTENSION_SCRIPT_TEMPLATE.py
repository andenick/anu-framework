def load_chopped_table(config: Dict) -> pd.DataFrame:
    """Load data from a Shaikh chopped table Excel file."""
    file_path = PROJECT_ROOT / config["file"]
    
    if not file_path.exists():
        raise FileNotFoundError(f"Chopped table not found: {file_path}")
    
    df = pd.read_excel(
        file_path,
        sheet_name=config.get("sheet", 0),
    )
    
    # Extract relevant column
    if "column" in config:
        if config["column"] in df.columns:
            series = df[["Year", config["column"]]].copy()
            series.columns = ["year", "value"]
        else:
            raise ValueError(f"Column {config['column']} not found in {file_path}")
    else:
        series = df
    
    return series


def load_shaikh_baseline(config: Dict) -> pd.DataFrame:
    """Load data from Shaikh's baseline absorbed data."""
    file_path = PROJECT_ROOT / config["file"]
    
    if not file_path.exists():
        raise FileNotFoundError(f"Baseline file not found: {file_path}")
    
    df = pd.read_csv(file_path)
    
