def apply_transform(df: pd.DataFrame, transform: str, **kwargs) -> pd.DataFrame:
    """Apply a transformation to the data."""
    if transform is None or transform == "none":
        return df
    
    if transform == "annual_average":
        # Convert monthly/quarterly to annual average
        if "year" in df.columns:
            return df.groupby("year").mean().reset_index()
        return df
    
    if transform == "annual_value":
        # Take year-end or specific period value
        if "year" in df.columns:
            return df.groupby("year").last().reset_index()
        return df
    
    if transform == "by_industry":
        # Filter BEA data to specific industry
        industry = kwargs.get("industry_filter")
        if industry and "IndustrYDescription" in df.columns:
            df = df[df["IndustrYDescription"].str.contains(industry, case=False)]
        return df
    
    # Add more transformations as needed
    return df


def splice_series(series_list: List[pd.DataFrame], splice_years: List[int], 
                  methods: Dict[int, str]) -> pd.DataFrame:
    """Splice multiple series together at specified years."""
    if len(series_list) == 0:
        return pd.DataFrame()
    
    if len(series_list) == 1:
        return series_list[0]
    
    combined = series_list[0].copy()
    
    for i, (splice_year, next_series) in enumerate(zip(splice_years, series_list[1:])):
        method = methods.get(splice_year, "growth_rate")
        
        if method == "growth_rate":
            # Apply growth rates from new series to continue from last baseline value
            if splice_year in combined["year"].values:
                base_value = combined[combined["year"] == splice_year]["value"].iloc[0]
                next_at_splice = next_series[next_series["year"] == splice_year]["value"].iloc[0]
                ratio = base_value / next_at_splice if next_at_splice != 0 else 1
                
                extension = next_series[next_series["year"] > splice_year].copy()
                extension["value"] = extension["value"] * ratio
                combined = pd.concat([combined, extension], ignore_index=True)
        
        elif method == "level_match":
            # Direct level matching, no adjustment
            extension = next_series[next_series["year"] > splice_year].copy()
            combined = pd.concat([combined, extension], ignore_index=True)
    
    return combined.sort_values("year").reset_index(drop=True)


